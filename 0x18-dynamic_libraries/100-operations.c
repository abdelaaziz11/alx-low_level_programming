#include <stdio.h>

/**
 * mul - mutplication
 * Return: number
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * add - addition
 * Return: number
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * min - minus
 * Return: number
 */
int min(int a, int b)
{
	return (a - b);
}
/**
 * div - division
 * Return: number
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulas
 * Return: number
 */
int mod(int a, int b)
{
	return (a % b);
}
